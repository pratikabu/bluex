private void cmdExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdExitActionPerformed
// TODO add your handling code here:
    //with beep
    Toolkit.getDefaultToolkit().beep();
    int choice=JOptionPane.showConfirmDialog(this,
            "WARNING: Killing a running uniterruptable thread can cause" +
            "undersired results including loss of data and object instability." +
            "The thread will not be given the chance to save its state or\n" +
            "data before it is terminated." +
            "Are you sure you want to kill (the) thread(s)?", "Exit Warning", JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);

    if(choice==JOptionPane.YES_OPTION){
        exit=true;
        this.dispose();
    }
}//GEN-LAST:event_cmdExitActionPerformed

