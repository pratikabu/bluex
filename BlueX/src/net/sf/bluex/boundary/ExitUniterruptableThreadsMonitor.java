    private void initListData(){
        java.util.Vector<String> runningThreads=new java.util.Vector<String>();
        
        for(Thread t : MyThread.getRunningThreads(UninterruptableThread.class)){
                runningThreads.add(t.getName());
        }
        
        lstThreads.setListData(runningThreads);
    }
    
    public static boolean showRunningThreads(java.awt.Frame parent){
        if(eutm==null)
            eutm=new ExitUniterruptableThreadsMonitor(parent);
        if(eutm.isVisible())
            eutm.requestFocus();
        else
            eutm.setVisible(true);
        
        return eutm.exit;
    }

