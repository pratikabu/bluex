    private void doSaveJob() {
        BlueXStatics.prop.setProperty("laf", lafs[jComboBox1.getSelectedIndex()].getClassName());
        BlueXStatics.prop.setProperty("iconSet", (jComboBox2.getSelectedIndex()+1)+"");
        BlueXStatics.prop.setProperty("allocationUnit", jcbAllocationUnit.getSelectedItem().toString());
        BlueXStatics.prop.setProperty("checkPlugins", jCheckBox2.isSelected()+"");
        BlueXStatics.prop.setProperty("showHidden", jCheckBox5.isSelected()+"");
        BlueXStatics.prop.setProperty("displayFullPath", jCheckBox4.isSelected()+"");
        BlueXStatics.prop.setProperty("browseFolders", jRadioButton1.isSelected()?0+"":1+"");
        BlueXStatics.prop.setProperty("redirectURL", jCheckBox6.isSelected()+"");
        BlueXStatics.prop.setProperty("proxyEnabled", jCheckBox1.isSelected()+"");
        BlueXStatics.prop.setProperty("host", jTextField1.getText());
        BlueXStatics.prop.setProperty("port", jTextField2.getText());

        BlueXStatics.saveProperties();
        BlueXStatics.initProxy();

        //close
        this.dispose();
    }

