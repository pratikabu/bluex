    private void renderPreviousData() {

        boolean lafFound=false;
        for (LookAndFeelInfo laf : lafs)
            if (laf.getClassName().equals(BlueXStatics.prop.getProperty("laf"))){
                jComboBox1.setSelectedItem(laf.getName());
                lafFound=true;
                break;
            }
        
        if(!lafFound)
            for(LookAndFeelInfo laf : lafs)
                if(laf.getClassName().equals(UIManager.getSystemLookAndFeelClassName()))
                    jComboBox1.setSelectedItem(laf.getName());

        int browseFolders=Integer.parseInt(BlueXStatics.prop.getProperty("browseFolders"));
        if(browseFolders==0)
            jRadioButton1.setSelected(true);
        else
            jRadioButton2.setSelected(true);

        jCheckBox4.setSelected(Boolean.parseBoolean(BlueXStatics.prop.getProperty("displayFullPath")));
        jCheckBox5.setSelected(Boolean.parseBoolean(BlueXStatics.prop.getProperty("showHidden")));

        int iconSet=Integer.parseInt(BlueXStatics.prop.getProperty("iconSet"));
        jComboBox2.setSelectedIndex(iconSet-1);

        jcbAllocationUnit.setSelectedItem(BlueXStatics.prop.getProperty("allocationUnit"));

        jCheckBox2.setSelected(Boolean.parseBoolean(BlueXStatics.prop.getProperty("checkPlugins")));

        Color currentColor=new Color(Integer.parseInt(BlueXStatics.prop.getProperty("hiddenColor")));
        jPanel7.setBackground(currentColor);

        jCheckBox6.setSelected(Boolean.parseBoolean(BlueXStatics.prop.getProperty("redirectURL")));

        //proxy
        boolean proxyEnabled=Boolean.parseBoolean(BlueXStatics.prop.getProperty("proxyEnabled"));
        jCheckBox1.setSelected(proxyEnabled);
        jTextField1.setText(BlueXStatics.prop.getProperty("host"));
        jTextField2.setText(BlueXStatics.prop.getProperty("port"));
    }

