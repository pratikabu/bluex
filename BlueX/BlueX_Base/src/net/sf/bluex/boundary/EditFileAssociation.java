    private void initPlugins(boolean extensionAssociated, String pluginName){
        Vector<String> vectPlugins=new Vector<String>();

        if(extensionAssociated){
            for(FileAssociation tempFA : UsefulMethods.getAllFileAssociations(jTextField1.getText()))
                vectPlugins.add(tempFA.getPluginName());
            jButton1.setText("List All Plugins");
        }else{
            for(PluginMetaData pmd : UsefulMethods.getAllInstalledPlugins(ModuleDetail.FILE_ASSOCIATION_PLUGIN))
                vectPlugins.add(pmd.getName());
            jButton1.setText("List Associated Plugins");
        }

        jList1.setListData(vectPlugins);
        if(pluginName!=null)
            jList1.setSelectedValue(pluginName, true);
    }

    private void renderPreviousFA(){
        jTextField1.setEditable(isNew);
        String pluginName=null;

        if(!isNew){
            jTextField1.setText(fa.getExtension());

            if(fa.getPluginName().equals("Operating System"))
                jRadioButton1.setSelected(true);
            else{
                jRadioButton2.setSelected(true);
                pluginName=fa.getPluginName();
            }
        }

        initPlugins(!isNew, pluginName);
    }

