    private boolean entriesCorrect() {
        StringBuffer error=new StringBuffer("");
        if(jTextField1.getText().trim().isEmpty())
            error.append("- You cannot leave the Extension empty.");
        if(jRadioButton2.isSelected() && jList1.getSelectedIndex()==-1)
            error.append("- Select any plugin from the list to associate with.");

        if(error.length()!=0){
            JOptionPane.showMessageDialog(this, "There are some errors:\n\n"+error.toString(), "Form not Complete", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }
