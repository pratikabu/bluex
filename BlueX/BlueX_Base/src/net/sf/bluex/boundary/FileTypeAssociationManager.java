    private void updateTableData() {
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
        for(int i=dtm.getRowCount()-1; i>=0; i--)
            dtm.removeRow(i);

        for(Entry<Object, Object> ent : BlueXStatics.fa.entrySet())
            dtm.addRow(new String[]{ent.getKey().toString(), ent.getValue().toString()});
    }

