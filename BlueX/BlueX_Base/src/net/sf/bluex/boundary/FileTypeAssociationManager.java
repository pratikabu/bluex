private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    // TODO add your handling code here:
    int row=jTable1.getSelectedRow();
    if(row!=-1){
        FileAssociation fa=EditFileAssociation.showEditFileAssociation(this, new FileAssociation((String)jTable1.getValueAt(row, 1), (String)jTable1.getValueAt(row, 0)));
        BlueXStatics.fa.setProperty(fa.getExtension(), fa.getPluginName());
        updateTableData();
    }else
        JOptionPane.showMessageDialog(this, "Select any row.");
}//GEN-LAST:event_jButton3ActionPerformed

