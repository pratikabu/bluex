    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        //prepare the pmd
        pmd.setName(jTextField1.getText());
        pmd.setAuthor(jTextField4.getText());
        pmd.setVersion(new Version(jTextField5.getText()));
        pmd.setPluginClass((String)jComboBox1.getSelectedItem());
        pmd.setMinimumBaseWindowVersion(new Version(jTextField6.getText()));
        pmd.setHelpAvailable(jCheckBox1.isSelected());
        pmd.setPluginDescription(jTextArea1.getText());

        if(jCheckBox2.isSelected())
            pmd.addModule(new ModuleDetail(ModuleDetail.PANEL_BASED_PLUGIN));
        if(jCheckBox3.isSelected())
            pmd.addModule(new ModuleDetail(ModuleDetail.NEW_WINDOW_PLUGIN));
        if(jCheckBox4.isSelected())
            pmd.addModule(md);

        //saving
        JFileChooser jfc=new JFileChooser();
        int choice=jfc.showSaveDialog(this);
        if(choice==jfc.APPROVE_OPTION){
            if(PluginSaving.savePlugin(pmd, jfc.getSelectedFile()))
                JOptionPane.showMessageDialog(this, "Successfully Saved.");
            else
                JOptionPane.showMessageDialog(this, "There is some problem while saving.");
        }
    }//GEN-LAST:event_jButton12ActionPerformed

