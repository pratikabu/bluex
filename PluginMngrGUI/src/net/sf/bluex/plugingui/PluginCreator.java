    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc=new JFileChooser();
        jfc.setFileFilter(new FileNameExtensionFilter("Plugin Jar Files", "jar"));
        int choice=jfc.showOpenDialog(this);
        if(choice==jfc.APPROVE_OPTION){
            pluginJarFile=jfc.getSelectedFile();
            Vector<File> jarFiles=new Vector<File>();
            jarFiles.add(pluginJarFile);
            Vector<String> suggestedPluginClasses=newComponents.components.ExplorerJarFile.getSuggestedClasses(jarFiles, Plugin.class);
            for(String pc : suggestedPluginClasses)
                jComboBox1.addItem(pc);
            jTextField3.setText(pluginJarFile.getName());
            pmd.setJarFileName(pluginJarFile.getName());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

